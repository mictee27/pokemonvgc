export const MEGA_STONES = LEGAL_ITEMS.filter(i => i.isMegaStone)

// Map: item name → species id it belongs to
export const MEGA_STONE_SPECIES_MAP = new Map(
  MEGA_STONES.filter(i => i.megaFor).map(i => [i.name, i.megaFor!])
)

// Map: species id → their mega stone name(s)
export const SPECIES_MEGA_STONES = new Map<string, string[]>()
for (const item of MEGA_STONES) {
  if (!item.megaFor) continue
  const existing = SPECIES_MEGA_STONES.get(item.megaFor) ?? []
  SPECIES_MEGA_STONES.set(item.megaFor, [...existing, item.name])
}
